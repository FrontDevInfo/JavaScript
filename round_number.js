// Math.floor
console.log(
    Math.floor(1.5), // 1
    Math.floor(9.6), // 9
    Math.floor(7.3) // 7
)

// Math.ceil()
console.log(
    Math.ceil(7.5), // 8
    Math.ceil(3.6), // 4
    Math.ceil(1.3), // 2
    Math.ceil(-0.65),  // -0
    Math.ceil(-5.001) // -5
)

// Math.trunc
console.log(
    Math.trunc(0.5), // 0
    Math.trunc(2.67), // 2
    Math.trunc(7.8), // 7
)

// Math.round
console.log(
    Math.round(0.5), // 1
    Math.round(2.6), // 3
    Math.round(8.36), // 8
)
